-- recount with the fallback tightened by geographic footprint
WITH fallback_footprint AS (
    SELECT HCO_PARENT_NAME,
           COUNT(DISTINCT PRIMARY_HCO_NPI_STATE) AS STATES
    FROM COMPILE_DEV.PUBLIC.ATC_CLASSIFIED_HYBRID
    WHERE CLASS_HYBRID = 'ATC: name fallback'
    GROUP BY 1
)
SELECT
    CASE
        WHEN h.CLASS_HYBRID = 'ATC: NPI confirmed' THEN 'ATC'
        WHEN h.CLASS_HYBRID = 'ATC: name fallback' AND f.STATES <= 2 THEN 'ATC'
        WHEN h.CLASS_HYBRID = 'ATC: name fallback' AND f.STATES > 2 THEN 'Non-ATC (system sweep)'
        WHEN h.CLASS_HYBRID LIKE 'Non-ATC%' THEN 'Non-ATC'
        WHEN h.CLASS_HYBRID = 'Needs Review' THEN 'Needs Review'
        ELSE 'Non-ATC'
    END AS CLASS_FINAL,
    COUNT(DISTINCT h.D_PATIENT_ID) AS PATIENTS,
    ROUND(100.0*COUNT(DISTINCT h.D_PATIENT_ID)/SUM(COUNT(DISTINCT h.D_PATIENT_ID)) OVER(),1) AS PCT
FROM COMPILE_DEV.PUBLIC.ATC_CLASSIFIED_HYBRID h
LEFT JOIN fallback_footprint f ON h.HCO_PARENT_NAME = f.HCO_PARENT_NAME
GROUP BY 1 ORDER BY 2 DESC;