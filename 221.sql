SELECT
    "NPI",
    "ATC Name (IovanceCares)"  AS ATC_NAME,
    "ATC HCO Parent Name (McKesson Claims)" AS PARENT_NAME,
    "Status"
FROM COMPILE_DEV.PUBLIC.CTAM_ATC_ALIGNMENT_2026
WHERE "NPI" IS NOT NULL
  AND "NPI" <> 'NPI'          -- drop the header-as-data row
  AND TRIM("NPI") <> '0'
  AND TRIM("NPI") <> ''
LIMIT 20;


WITH authorized_npis AS (
    SELECT DISTINCT TRIM("NPI") AS NPI
    FROM COMPILE_DEV.PUBLIC.CTAM_ATC_ALIGNMENT_2026
    WHERE UPPER(TRIM("Status")) = 'AUTHORIZED'
      AND "NPI" IS NOT NULL
      AND TRIM("NPI") NOT IN ('0','','NPI')
)
SELECT
    CASE WHEN a.NPI IS NOT NULL THEN 'ATC (NPI match)' ELSE 'Non-ATC' END AS CLASS,
    COUNT(DISTINCT p.D_PATIENT_ID) AS PATIENTS
FROM COMPILE_DEV.PUBLIC.ATC_SOC_PATIENT_CLASSIFIED_2021_2025 p
LEFT JOIN authorized_npis a
    ON TRIM(p.D_PRIMARY_HCO_NPI) = a.NPI
GROUP BY 1;